<script>

	//Set options by id country
	function setOptionsState(states, old_value=null){
		var states = JSON.parse(states);
		$('#state').empty();
		$('#state').append($('<option>', { 
			value: 'select',
			text : 'Select'
		}));
		states.forEach(element => {
			if(old_value != null && element.id == old_value){
				console.log(old_value);
				$('#state').append($('<option >', { 
					value: element.id,
					text : element.name,
					selected: true
				}));
			}else{
				$('#state').append($('<option>', { 
					value: element.id,
					text : element.name 
				}));
			}
		});
	}
	//

	//Set options by id state
	function setOptionsCity(cities, old_value = null){
		var cities = JSON.parse(cities);
		$('#city').empty();
		$('#city').append($('<option>', { 
			value: 'select',
			text : 'Select'
		}));
		cities.forEach(element => {
			if(old_value != null && element.id == old_value){
				$('#city').append($('<option>', { 
					value: element.id,
					text : element.name,
					selected: true
				}));

			}else{
				$('#city').append($('<option>', { 
					value: element.id,
					text : element.name,
				}));
			}
		});
	}

	//Invoke info for the children select and provide info
		$('#country').on('change',function(){	
			var country = $('#country').val();
			$.get("/state/"+country, function(data){
				if(data.length === 2){
					//If in db no have data about states for the selected country then city and states are unable
					$('#state').empty();
					$('#state').append($('<option>', { 
						value: 'N/A',
						text : 'No available States',
					}));
					$('#city').empty();
					$('#city').append($('<option>', { 
						value: 'N/A',
						text : 'No available Cities',
					}));
					//
				}else{
					setOptionsState(data);

					//If the country change and the state equal, then cities select return a default state
					$('#city').empty();
					$('#city').append($('<option>', { 
						value: 'select',
						text : 'Select a State first',
					}));
				}
			});
		});


		$('#state').on('change',function(){
			var state = $('#state').val();
			console.log(state);
			$.get("/city/"+state, function(data){
				if(data.length === 2){
					$('#city').empty();
					$('#city').append($('<option>', { 
						value: 'N/A',
						text : 'No available Cities',
					}));
				}else{
					setOptionsCity(data);
				}
			});
		});
